package All_Loops;

public class While_Loops {

    public static void main(String[] args) {

        //SYNTAX : SYNTAX:  while (condition){
        //                                   //code
        //			                  increment/decrement;
        //                         }

        int i=1;

        while (i<=10){

            System.out.println(5 + " * " + i + " = " + (5 * i ));

            i++;
        }
        
    }
}
