package Loops_Demo;

public class Even_and_Odd_number {

    public static void main(String[] args) {


        // print even numbers 1 to 100

        int number = 100;

        for (int i = 1; i <= 100; i++) {
            if (i % 2 == 0) {
                System.out.println(i + "");
            }
        }

        // print odd numbers 1 to 100

        number=100;
        for(int k=1; k<=100; k++) {
            if (k % 2 != 0) {
                System.out.println(k + "");
            }
        }








    }
}


