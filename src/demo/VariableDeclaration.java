package demo;

public class VariableDeclaration {

    public static void main(String[] args) {

        int a;
        a=45;
        System.out.println("DECLARING VALUE OF A:"+ a);

        int b;
        b=75;
        System.out.println("DECLARING VALUE OF B:"+ b);

        String city = "JAIPUR";
        System.out.println("CAPITAL OF RAJASTHAN:"+ city);

        String country = "INDIA";
        System.out.println("COUNTRY NAME:"+ country);

        double weight = 70.5;
        System.out.println("WEIGHT:"+ weight);
    }
}
