package gitvariable;

public class VariableIncreementOperator {

    public static void main(String[] args) {

        // Increement operator

        int
                i=1;
        System.out.println("Printing before increement operator : "+i);

        i++;
        System.out.println("PRinting after increement operator : "+i);

        // Decreement operator

        i=1;
        System.out.println("Printing before decreement operator : "+i);
        i--;
        System.out.println("Printing after decreement operator : "+i);







    }










}
