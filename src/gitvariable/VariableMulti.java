package gitvariable;

public class VariableMulti {

    public static void main(String[] args) {

        int a=96;
        int b=65;
        int multi=a*b;
        System.out.println("Printing multiplication : "+multi);

        a=64;
        b=36;
        int result=a*b;
        System.out.println("Printing multiplication : "+result);



    }








}
