package gitvariable;

public class VariablePercentageCalculate {

    public static void main(String[] args) {

        int number = 965;
        int percentage = 10;
        double result = (double) (number * percentage) / 100;
        System.out.println("Printing percentage : " + result);

        number = 65489;
        percentage = 65;
        double r = (double) (number * percentage) / 100;
        System.out.println("Printing percentage : " + r);


    }


}
