package loops;

public class DemoLoops {

    public static void main(String[] args) {

     //increment for loops


        for (int i = 1; i <= 100; i++) {

            System.out.println("even number" + i);
        }

      // decrement for loops

        for(int j=1; j>=100; j--){
            System.out.println("odd number"+j);
        }
    }
}