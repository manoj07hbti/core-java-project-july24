package loops;

public class Demo_Loops {

    public static void main(String[] args) {




//SYNTAX:   for (start_condition; conditional_expression; increment){
//                 //CODE
//              }


     // MY INCREMENT LOOP

        for (int i=0; i<=100 ; i++){
            System.out.println("hi himanshu.."+i);
        }

        // SYNTAX:   for (start_condition; conditional_expression; decrement){
//                 //CODE
//              }

        for(int i=50; i>0 ; i--){

            System.out.println("I,M HIMANSHU "+i);
        }

       // 2 INCREMENT
      for(int h=1; h<1001; h++){
          System.out.println("welcome to java word"+h);
      }

      // 2 DECREMENT

        for(int k=20; k>0; k--){
            System.out.println("you are not entered to java"+k);
        }

        // 3 INCREMENT
        for(int q=5; q<=55; q++){
            System.out.println("welcome to agra"+q);
        }

        // 3 DECREMENT

        for(int e=55; e>0; e--){
            System.out.println("welcome to pune"+e);
        }







    }





    }



