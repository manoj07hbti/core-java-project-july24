package loops;

public class ForLoopsAssignment {

    public static void main(String[] args) {

        int number=5;
        int result;

        for(int i=1; i<=10; i++){
            result=number*i;
            System.out.println("5" + "*" +i + "=" +(result));
        }

         number=10;
        for(int j=1; j<=10; j++){
            result=number*j;
            System.out.println("10" + "*" +j + "=" +(result));
        }

        }


    }



