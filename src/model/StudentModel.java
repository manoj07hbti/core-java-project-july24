package model;

public class StudentModel {

    String name;
    int age;
    String city;
    String department;

    public StudentModel(String name, int age, String city, String department) {
        this.name = name;
        this.age = age;
        this.city = city;
        this.department = department;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }
}
