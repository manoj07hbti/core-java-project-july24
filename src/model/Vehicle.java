package model;

public class Vehicle {
   public String engineNumber;
  public   int power;
  public   int mileage;
  public   String type;

    public Vehicle(String engineNumber, int power, int mileage, String type) {
        this.engineNumber = engineNumber;
        this.power = power;
        this.mileage = mileage;
        this.type = type;
    }
}
