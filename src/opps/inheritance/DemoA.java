package opps.inheritance;

public class DemoA {

    public void h1() {
        System.out.println("this is method of class A");
    }

    public void h2() {
        System.out.println("This is my method");
    }


    protected void h3() {
        System.out.println("This is my Protected class");
    }

    protected void h4() {
        System.out.println("This is my Second Protected class");
    }
}

