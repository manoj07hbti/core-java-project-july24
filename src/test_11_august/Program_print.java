package test_11_august;

public class Program_print {

    public static void main(String[] args) {


        int i=-3;
        do {
            System.out.println("DECREMENT NUMBER." + i);
            i--;
        } while (i>=0);


        do {
            System.out.println("INCREMENT NUMBER." + i);
            i++;
        } while (i <= 3);


    }

}
