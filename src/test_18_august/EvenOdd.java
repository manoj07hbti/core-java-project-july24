package test_18_august;

public class EvenOdd {

    public static void main(String[] args) {

        for (int num = 300; num >= 10; num--) {

            if (num % 2 == 0) {
                System.out.println(num + " is a even number ");
            } else {
                System.out.println(num + " is a odd number");
            }


        }
    }

}

